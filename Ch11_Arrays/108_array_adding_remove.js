let arr = [1, 2, 3];
console.log(arr);

//add to end
arr.push(4);
console.log(arr);

//remove from end
arr.pop();
console.log(arr);


//add multiple to end
arr.push(4, 5);
console.log(arr);


//Add to beginning
arr.unshift(0);
console.log(arr);


//remove from beginning
arr.shift(1);
console.log(arr);
