//pre increment , increase the value then use it
let a = 10;
console.log("Pre Increment:", ++a); // Output: Pre Increment: 11
console.log("Current value of a:", a); // Output: Current value of a: 11    

//post increment, use the value then increase it
let b = 20;
console.log("Post Increment:", b++); // Output: Post Increment: 20
console.log("Current value of b:", b); // Output: Current value of b: 21