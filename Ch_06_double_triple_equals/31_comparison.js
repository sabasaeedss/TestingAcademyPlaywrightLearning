// > , <, <=, >=, ==, ===, !=, !==, !===
// = => Assignment operator
// == (loose equality) checks for value equality, allowing type coercion if necessary.
// === (strict equality) checks for both value and type equality, without allowing type coercion.

console.log(3 > 4); // Output: false
console.log(3 < 4); // Output: true
console.log(3 >= 3); // Output: true
console.log(3 <= 4); // Output: true 3<4 or 3===4