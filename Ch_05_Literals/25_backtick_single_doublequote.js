// Single Quote, Double Quote, and Backtick in JavaScript

// 1. Single Quote ('')
// Use: Simple string literals. No variable interpolation.
let single = 'Hello, World!';
console.log(single);

// 2. Double Quote ("")
// Use: Simple string literals. Same as single quote, just different delimiter.
let double = "Hello, World!";
console.log(double);

// 3. Backtick (``)
// Use: Template literals. Supports variable interpolation, multi-line strings, and expressions.
let name = "Alice";
let backtick = `Hello, ${name}!`;
console.log(backtick);
